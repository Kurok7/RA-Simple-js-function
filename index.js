//using international measurements for height

function user(name, height, country) {
	alert(
		"My name is " +
			name +
			". I am " +
			height +
			"cm tall and I am from " +
			country +
			"."
	);
}

user("Juan", 172, "America");
user("Cherry", 154, "Thailand");
user("Daniel", 160, "Ethiopia");
